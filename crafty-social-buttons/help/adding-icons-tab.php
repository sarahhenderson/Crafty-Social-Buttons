<p><strong>Adding new icons</strong></p>

<p>Including a new set of icons is pretty easy!  The requirements are:</p>
<ol>
	<li>Your images are <strong>.png</strong> files</li>
    <li>You use the same filenames as the existing image sets (which are pretty straighforward - e.g. facebook is facebook.png, ravelry is ravelry.png)</li>
    <li>Your images are either 32x32px or 64x64px in size. (This isn't strictly necessary, but will give you the best results.  By default, the images are displayed at 32x32px.</li>
</ol>

<p>If you've got all that sorted, you just need to create a new folder inside the <code>wp-plugins/crafty-social-buttons/buttons</code> folder.  You'll see the folders for the existing icon sets - just add your new folder beside them.  It is best if you don't have any spaces or punctuation in the folder name.</p>

<p>Now if you refresh this options page, you will see your folder name as one of the options to choose from!</p>
